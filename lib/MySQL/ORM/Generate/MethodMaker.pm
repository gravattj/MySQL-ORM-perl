method make_method (Str   :$name, 
		     	 	Str   :$sig, 
				 	Str   :$body ) {

	my $text;
	$text .= "method $name (\n";

	if ($sig) {
		$sig = trim $sig;
		$text .= "    $sig\n";
	}

	$text .= ") {\n";

	if ($body) {
		$body = trim $body;
		$text .= "$body\n";
	}

	$text .= "}";

	return $text;
}


